def setup_test_logging():
    """Konfiguruje logowanie dla test√≥w"""
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
        handlers=[logging.StreamHandler(sys.stdout)]
    )


class MPScraperTester:
    """Klasa do testowania MP Scrapera"""

    def __init__(self):
        self.scraper = MPScraper()
        self.api = SejmAPI()
        self.test_results = []

    def run_test(self, test_name: str, test_func, *args, **kwargs):
        """
        Uruchamia pojedynczy test i zbiera wyniki

        Args:
            test_name: nazwa testu
            test_func: funkcja testowa
            *args, **kwargs: argumenty dla funkcji testowej
        """
        print(f"\n{'=' * 60}")
        print(f"üß™ TEST: {test_name}")
        print(f"{'=' * 60}")

        start_time = time.time()

        try:
            result = test_func(*args, **kwargs)
            duration = time.time() - start_time

            if result:
                status = "‚úÖ PASSED"
                print(f"\n{status} - Czas: {duration:.2f}s")
            else:
                status = "‚ùå FAILED"
                print(f"\n{status} - Czas: {duration:.2f}s")

            self.test_results.append({
                'test': test_name,
                'status': 'PASSED' if result else 'FAILED',
                'duration': duration,
                'result': result
            })

            return result

        except Exception as e:
            duration = time.time() - start_time
            status = "üí• ERROR"
            print(f"\n{status} - B≈ÇƒÖd: {e}")
            print(f"Czas: {duration:.2f}s")

            self.test_results.append({
                'test': test_name,
                'status': 'ERROR',
                'duration': duration,
                'error': str(e)
            })

            return False

    def test_api_connection(self) -> bool:
        """Test podstawowej ≈ÇƒÖczno≈õci z API"""
        print("Sprawdzanie po≈ÇƒÖczenia z API Sejmu...")

        terms = self.api.get_terms()
        if not terms:
            print("‚ùå Nie mo≈ºna pobraƒá listy kadencji")
            return False

        print(f"‚úÖ Pobrano {len(terms)} kadencji")

        # Sprawd≈∫ aktualnƒÖ kadencjƒô
        current_term = None
        for term in terms:
            if term.get('current'):
                current_term = term
                break

        if current_term:
            print(f"üìç Aktualna kadencja: {current_term['num']}")
        else:
            print("‚ö†Ô∏è  Nie znaleziono aktualnej kadencji")

        return True

    def test_clubs_api(self, term: int = DEFAULT_TERM) -> bool:
        """Test pobierania klub√≥w przez API"""
        print(f"Testowanie API klub√≥w dla kadencji {term}...")

        clubs = self.api.get_clubs(term)
        if not clubs:
            print("‚ùå Nie mo≈ºna pobraƒá listy klub√≥w")
            return False

        print(f"‚úÖ Znaleziono {len(clubs)} klub√≥w")

        # Test szczeg√≥≈Ç√≥w pierwszego klubu
        if clubs:
            first_club = clubs[0]
            club_id = first_club.get('id')
            club_name = first_club.get('name', 'Unknown')

            print(f"üîç Testowanie szczeg√≥≈Ç√≥w klubu: {club_name}")

            club_details = self.api.get_club_info(term, club_id)
            if club_details:
                print(f"‚úÖ Pobrano szczeg√≥≈Çy klubu {club_name}")
            else:
                print(f"‚ö†Ô∏è  Brak szczeg√≥≈Ç√≥w dla klubu {club_name}")

            # Test logo klubu
            logo = self.api.get_club_logo(term, club_id)
            if logo and isinstance(logo, bytes):
                print(f"‚úÖ Pobrano logo klubu {club_name} ({len(logo)} bajt√≥w)")
            else:
                print(f"‚ö†Ô∏è  Brak logo dla klubu {club_name}")

        return True

